/* ── Fetcher ──
   Supabase 연동 시 함수 내부만 교체한다. 예:

   export async function getRankingsByDivision(division) {
     const { data, error } = await supabase
       .from("rankings")
       .select("*")
       .eq("division", division)
       .order("rank", { ascending: true });
     if (error) throw error;
     return data as RankingPlayer[];
   }
*/

/** 특정 디비전의 랭킹을 순위 오름차순으로 반환한다. */
export async function getRankingsByDivision(
  division: Division,
): Promise<RankingPlayer[]> {
  return [...RANKINGS[division]].sort((a, b) => a.rank - b.rank);
}

/**
 * 전체 디비전의 랭킹을 한 번에 반환한다.
 * 페이지에서 탭 전환 시 추가 요청 없이 즉시 표시할 수 있도록
 * 모든 디비전 데이터를 미리 로딩하기 위한 용도.
 */
export async function getAllDivisionRankings(): Promise<
  Record<Division, RankingPlayer[]>
> {
  return {
    아마추어: await getRankingsByDivision("아마추어"),
    마스터즈: await getRankingsByDivision("마스터즈"),
    우먼스: await getRankingsByDivision("우먼스"),
    오픈: await getRankingsByDivision("오픈"),
  };
}
